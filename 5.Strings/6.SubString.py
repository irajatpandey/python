# check string is a substring or not

# Python’s in and not in operators allow you to quickly determine if a given value is or isn’t part of a collection of values. 
# This type of check is common in programming, and it’s generally known as a membership test in Python. 
# Therefore, these operators are known as membership operators.
str = "hello"

if 'llo' in str:
    print("Yes")
else:
    print("No")